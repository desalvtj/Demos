public class FormattedOutput
{
    public static void main(String[] args)
    {
        int age = 15;
        double salary = 45000.236;

        for(int c = 0; c <= 10; c++)

            System.out.printf("%-10s %10.2f %5d\n", "Tom", salary, age);



            // s - string
            // f - float
            // d = digit
    }

}
